WS.verifyMatch(WebUI.getUrl(), GlobalVariable.application_domain +'/team/.*/project/.*/executions/.*(?:#.*)?(?:\\?.*)?$', true)

WebUI.enhancedClick(testObj)

'step 5: At Page_test-result-verifycustomsortonsearchresults click on button_select_organization'

testObj = findTestObject('Object Repository/Page_team_project_executions_test-result/button_object_37')

